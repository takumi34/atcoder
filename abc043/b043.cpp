int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  string s;
  cin >> s;

  vector<int> v;

  int ss = s.size();

  rep(i, ss) {
    if (s[i] == '0') {
      v.push_back(0);
    } else if (s[i] == '1') {
      v.push_back(1);
    } else {
      if (v.size() == 0) { continue; }
      v.pop_back();
    }
  }

  for (int i : v) { cout << i; }
  cout << endl;

  return 0;
}